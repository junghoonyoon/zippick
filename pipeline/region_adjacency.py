# 경기 일반구 → 대표 시 접기용 접두사.
_CITY_PREFIXES = ("고양", "부천", "성남", "수원", "안산", "안양", "용인")

# 시/도 광역 선택은 인접 추천 대상에서 제외한다(이미 범위가 넓어서).
_BROAD_REGIONS = {"서울", "서울시", "서울특별시", "경기", "경기도"}


def normalize_region(value):
    """지역 문자열을 인접 맵의 대표 키로 정규화한다. 미지원 지역은 ""."""
    name = str(value or "").strip().replace(" ", "")
    if not name or name in _BROAD_REGIONS:
        return ""
    for prefix in ("서울특별시", "서울시", "경기도", "경기"):
        if name.startswith(prefix):
            name = name[len(prefix):]
            break
    if name in ADJACENT_REGIONS:
        return name
    # "성남분당구", "고양 일산동구" 같은 일반구 표기를 대표 시로 접는다.
    for prefix in _CITY_PREFIXES:
        if name.startswith(prefix):
            city = f"{prefix}시"
            if city in ADJACENT_REGIONS:
                return city
    return ""


def adjacent_to(regions):
    """선택 지역 목록의 인접 지역을 우선순위 순서로 돌려준다(선택 지역 제외)."""
    selected = {normalize_region(item) for item in regions}
    selected.discard("")
    result = []
    seen = set(selected)
    for region in regions:
        key = normalize_region(region)
        for neighbor in ADJACENT_REGIONS.get(key, []):
            if neighbor not in seen:
                seen.add(neighbor)
                result.append(neighbor)
    return result
